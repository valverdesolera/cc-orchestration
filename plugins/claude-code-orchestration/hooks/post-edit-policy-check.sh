#!/usr/bin/env bash
set -euo pipefail

json_input="$(cat)"
file_path="$(JSON_INPUT="$json_input" python3 -c 'import json, os
try:
    data=json.loads(os.environ.get("JSON_INPUT", "{}"))
except Exception:
    data={}
ti=data.get("tool_input",{}) or {}
print(ti.get("file_path") or ti.get("notebook_path") or ti.get("path") or "")' 2>/dev/null || true)"

[[ -z "$file_path" ]] && exit 0
[[ ! -f "$file_path" ]] && exit 0

# Normalize Windows backslashes to forward slashes for pattern matching.
file_path_norm=$(printf %s "$file_path" | tr '\\' '/')

case "$file_path_norm" in
  *docs/ignored/*|*docs/temp/*|*.md|*.markdown|*.txt|*.json|*.lock|*.svg|*.png|*.jpg|*.jpeg|*.gif|*/hooks/*.sh)
    # Plugin hook scripts intentionally contain the forbidden patterns as regex
    # literals for matching; exempt them from self-scanning.
    exit 0
    ;;
esac

# Look for comments or strings that suggest plan/ticket/AI metadata leaked into code.
# This is intentionally conservative feedback; PostToolUse cannot prevent the edit, but it tells Claude to correct it.
# Patterns we never want to see in code comments or strings:
# - Implementation plan/stage/phase references
# - Workbook references and docs/ignored paths
# - Ticket IDs (Jira-style: 2-10 caps + dash + digits)
# - Branch-name patterns (feature/, fix/, chore/, etc.)
# - Plan filenames (Plan.md, Plan-Final.md, Stage-1-*.md)
# - "see the ticket", "per the plan", "as per stage X"
# - Claude / Anthropic / AI attribution
patterns='(ImplementationStep|implementation stage|implementation plan|docs/ignored|workbook|phase [0-9]+|stage [0-9]+|Plan(-v[0-9]+|-Final)?\.md|Stage-[0-9]+-[A-Za-z0-9_-]+\.md|[A-Z]{2,10}-[0-9]{2,6}|see (the )?ticket|per the plan|per the (stage|phase)|as per (stage|phase)|JIRA|Linear|(feature|fix|chore|hotfix|release|bugfix)/[A-Za-z0-9_-]+|Claude Code|Co-authored-by:.*(claude|anthropic|gpt|copilot)|AI-generated|generated by Claude|generated by AI|generated with (AI|claude|gpt)|🤖|🧠)'
if grep -inE "$patterns" "$file_path" >/tmp/claude-post-edit-policy.$$ 2>/dev/null; then
  printf 'Policy check warning for %s:\n' "$file_path" >&2
  printf 'Code MUST NOT contain comments or strings that reference implementation plans, tickets, phases, branch names, plan filenames, Claude, or AI attribution. If any of these are intentional product text (e.g. legitimate string literals that happen to match), explicitly justify in the PR description. Review these lines:\n' >&2
  cat /tmp/claude-post-edit-policy.$$ >&2
  rm -f /tmp/claude-post-edit-policy.$$
  exit 2
fi

rm -f /tmp/claude-post-edit-policy.$$ 2>/dev/null || true
exit 0
