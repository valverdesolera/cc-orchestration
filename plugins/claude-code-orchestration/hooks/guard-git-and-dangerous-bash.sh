#!/usr/bin/env bash
set -euo pipefail

json_input="$(cat)"
parsed="$(JSON_INPUT="$json_input" python3 -c 'import json, os
try:
    data=json.loads(os.environ.get("JSON_INPUT", "{}"))
except Exception:
    data={}
ti=data.get("tool_input",{}) or {}
agent=data.get("agent_type") or ""
if not agent and isinstance(data.get("agent"),dict): agent=data["agent"].get("name") or data["agent"].get("type") or ""
if not agent and isinstance(data.get("subagent"),dict): agent=data["subagent"].get("name") or data["subagent"].get("type") or ""
print(ti.get("command", ""))
print(agent)' 2>/dev/null || true)"
command="$(printf '%s\n' "$parsed" | sed -n '1p')"
agent_type="$(printf '%s\n' "$parsed" | sed -n '2p')"
prefix="${CLAUDE_OVERLAY_AGENT_PREFIX:-}"
agent_base="$agent_type"
[[ -n "$prefix" && "$agent_base" == "$prefix"* ]] && agent_base="${agent_base#$prefix}"
lower_command="$(printf '%s' "$command" | tr '[:upper:]' '[:lower:]')"

block() { printf '%s\n' "$1" >&2; exit 2; }

contains_ai_attribution() {
  grep -Eiq 'co-authored-by:.*(claude|anthropic)|claude code|anthropic|ai-generated|generated by claude|generated with claude|generated with ai'
}

contains_temp_or_forbidden_artifacts() {
  grep -Ev '(^|/)\.env\.(example|sample|template)(\..*)?$' |
  grep -E '^(docs/ignored/|docs/temp/|\.claude/worktrees/|\.claude/settings\.local\.json|tmp/|scratch/)|(^|/)\.module-context\.md$|(^|/)\.env($|\.)|\.(tmp|temp|scratch|log|bak)$|(^|/)(debug|temp|tmp|scratch)[-_].*\.(sh|bash|zsh|fish|ps1|py|js|ts|mjs|cjs|sql)$|(^|/)scripts/(debug|temp|tmp|scratch)[-_]'
}

contains_forbidden_markdown_doc() {
  grep -E '\.(md|markdown)$' | grep -Ev '^(CLAUDE\.md|\.claude/agents/.+\.md|\.claude/skills/.+\.md|\.claude/rules/.+\.md|\.claude/commands/.+\.md)$'
}

# Read-only/finder/reviewer agents can inspect and validate, but not mutate files or external PR/issue state through shell commands.
case "$agent_base" in
  codebase-researcher|code-reviewer|backend-bug-finder|frontend-bug-finder|performance-reviewer|pr-reviewer|pre-push-guardian)
    if [[ "${CLAUDE_ALLOW_READONLY_AGENT_WRITE:-}" != "1" ]]; then
      if printf '%s' "$lower_command" | grep -Eiq '(^|[;&|[:space:]])(sed[[:space:]][^;&|]*-i|perl[[:space:]][^;&|]*-pi|touch|mkdir|cp|mv|rm|tee|truncate|chmod|chown|install|npm[[:space:]]+install|pnpm[[:space:]]+install|yarn[[:space:]]+(add|install)|pip[[:space:]]+install|poetry[[:space:]]+add|uv[[:space:]]+add|cargo[[:space:]]+add|go[[:space:]]+get|prettier([^[:alnum:]]|$)|eslint([^[:alnum:]]|$)[^;&|]*--fix|ruff([^[:alnum:]]|$)[^;&|]*--fix|black([^[:alnum:]]|$)|isort([^[:alnum:]]|$)|gofmt([^[:alnum:]]|$)|rustfmt([^[:alnum:]]|$)|git[[:space:]]+(add|commit|push|merge|rebase|reset|clean|stash|tag)|gh[[:space:]]+pr[[:space:]]+(create|review|comment|edit|merge|close)|gh[[:space:]]+issue[[:space:]]+(create|edit|close|comment))'; then
        block "Blocked by Claude Code read-only agent shell guard: ${agent_type} may not run file/external mutation commands. To override a confirmed safe action, set CLAUDE_ALLOW_READONLY_AGENT_WRITE=1 outside Claude Code."
      fi
    fi
    ;;
esac

# Defensive blocks for destructive commands.
if [[ "${CLAUDE_ALLOW_DESTRUCTIVE_COMMAND:-}" != "1" ]]; then
  if printf '%s' "$lower_command" | grep -Eiq '(^|[;&|[:space:]])(rm[[:space:]]+-rf[[:space:]]+(/|~|\$home|\$env:userprofile)|git[[:space:]]+reset[[:space:]]+--hard|git[[:space:]]+clean[[:space:]]+([^;&|]*[[:space:]])?-[[:alnum:]]*f[[:alnum:]]*|git[[:space:]]+push([^[:alnum:]]|$).*--force|git[[:space:]]+push([^[:alnum:]]|$).*[[:space:]]-f([[:space:]]|$)|git[[:space:]]+push([^[:alnum:]]|$).*(--delete|--mirror)|git[[:space:]]+push[[:space:]]+[^;&|[:space:]]+[[:space:]]+:[^;&|[:space:]]+|remove-item([^[:alnum:]]|$).*(-recurse|-r)|del[[:space:]]+/s|rmdir[[:space:]]+/s)'; then
    block "Blocked by Claude Code command guard: destructive command requires explicit human confirmation outside the agent. To override for a confirmed safe operation, set CLAUDE_ALLOW_DESTRUCTIVE_COMMAND=1 outside Claude Code."
  fi
fi

# Database/data-mutation guard.
if [[ "${CLAUDE_ALLOW_DB_WRITE:-}" != "1" ]]; then
  if printf '%s' "$lower_command" | grep -Eiq '\b(psql|mysql|mariadb|sqlite3|sqlcmd|mongo|mongosh|redis-cli|prisma|alembic|sequelize|knex|typeorm|rails db:|django-admin|manage\.py|createdb|dropdb|liquibase|flyway|dbmate|atlas)\b'; then
    if printf '%s' "$lower_command" | grep -Eiq '\b(insert|update|delete|drop|truncate|alter|create|replace|merge|call|grant|revoke|migrate|migration|seed|backfill|flushall|flushdb|reindex|vacuum)\b'; then
      block "Blocked by Claude Code database guard: possible data/schema mutation requires explicit human confirmation. To override for a confirmed safe operation, set CLAUDE_ALLOW_DB_WRITE=1 outside Claude Code."
    fi
  fi
fi

scan_commit_message_files() {
  COMMAND_TEXT="$command" python3 -c 'import re, os
cmd=os.environ.get("COMMAND_TEXT", "")
pat=re.compile(r"(?:^|\s)(?:-F|--file)(?:=|\s+)(?:\"([^\"]+)\"|\047([^\047]+)\047|(\S+))")
for m in pat.finditer(cmd):
    p=next((g for g in m.groups() if g), None)
    if p and os.path.isfile(p): print(p)
' 2>/dev/null || true
}

if [[ "$lower_command" == *"git commit"* ]]; then
  staged_files="$(git diff --cached --name-only 2>/dev/null || true)"
  if [[ -z "$staged_files" ]]; then
    block "Blocked by Claude Code git guard: no staged files found for commit. Stage intended files explicitly and retry."
  fi
  if printf '%s\n' "$staged_files" | contains_temp_or_forbidden_artifacts >/dev/null; then
    printf 'Blocked by Claude Code git guard: staged files include temporary, env/secret, log, or ignored Claude artifacts:\n%s\n' "$staged_files" >&2
    exit 2
  fi
  if [[ "${CLAUDE_ALLOW_MARKDOWN_COMMIT:-}" != "1" ]] && printf '%s\n' "$staged_files" | contains_forbidden_markdown_doc >/dev/null; then
    printf 'Blocked by Claude Code git guard: staged Markdown documentation outside allowed Claude config requires explicit human approval. Staged files:\n%s\nTo override for a confirmed durable documentation commit, set CLAUDE_ALLOW_MARKDOWN_COMMIT=1 outside Claude Code.\n' "$staged_files" >&2
    exit 2
  fi
  user_name="$(git config user.name 2>/dev/null || true)"
  user_email="$(git config user.email 2>/dev/null || true)"
  if [[ -z "$user_name" || -z "$user_email" ]]; then
    block "Blocked by Claude Code git guard: git user.name and git user.email must be configured before committing."
  fi
  if printf '%s' "$command" | contains_ai_attribution; then
    block "Blocked by Claude Code git guard: commit command appears to include Claude/AI attribution."
  fi
  msg_files="$(scan_commit_message_files)"
  if [[ -n "$msg_files" ]]; then
    while IFS= read -r msg_file; do
      [[ -z "$msg_file" ]] && continue
      if cat "$msg_file" | contains_ai_attribution; then
        block "Blocked by Claude Code git guard: commit message file contains Claude/AI attribution: $msg_file"
      fi
    done <<< "$msg_files"
  fi
fi

if [[ "$lower_command" == *"git push"* ]]; then
  upstream="$(git rev-parse --abbrev-ref --symbolic-full-name @{u} 2>/dev/null || true)"
  range=""
  if [[ -n "$upstream" ]]; then
    range="$upstream..HEAD"
  elif git rev-parse --verify origin/HEAD >/dev/null 2>&1; then
    range="origin/HEAD..HEAD"
  elif git rev-parse --verify origin/main >/dev/null 2>&1; then
    range="origin/main..HEAD"
  elif git rev-parse --verify origin/master >/dev/null 2>&1; then
    range="origin/master..HEAD"
  fi
  if [[ -n "$range" ]]; then
    outgoing="$(git log --name-only --format= "$range" 2>/dev/null | sed '/^$/d' | sort -u || true)"
    if printf '%s\n' "$outgoing" | contains_temp_or_forbidden_artifacts >/dev/null; then
      printf 'Blocked by Claude Code git guard: outgoing commits include temporary, env/secret, log, or ignored Claude artifacts:\n%s\n' "$outgoing" >&2
      exit 2
    fi
    if [[ "${CLAUDE_ALLOW_MARKDOWN_COMMIT:-}" != "1" ]] && printf '%s\n' "$outgoing" | contains_forbidden_markdown_doc >/dev/null; then
      printf 'Blocked by Claude Code git guard: outgoing commits include Markdown documentation outside allowed Claude config. Outgoing files:\n%s\nTo override for a confirmed durable documentation push, set CLAUDE_ALLOW_MARKDOWN_COMMIT=1 outside Claude Code.\n' "$outgoing" >&2
      exit 2
    fi
    outgoing_messages="$(git log --format=%B "$range" 2>/dev/null || true)"
    if printf '%s' "$outgoing_messages" | contains_ai_attribution; then
      block "Blocked by Claude Code git guard: outgoing commit messages contain Claude/AI attribution. Amend manually before pushing."
    fi
  else
    last_msg="$(git log -1 --pretty=%B 2>/dev/null || true)"
    if printf '%s' "$last_msg" | contains_ai_attribution; then
      block "Blocked by Claude Code git guard: latest commit message contains Claude/AI attribution. Amend manually before pushing."
    fi
  fi
fi

exit 0
